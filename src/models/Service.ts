import mongoose from "mongoose";
import { Service } from "../interfaces";

const schema = new mongoose.Schema({
    price: {
        type: Number,
        required: true
    },
    category: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Category',
        required: true
    },
    hours: {
        type: Number,
        required: true
    },
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    }
});

const Service = mongoose.model<Service>('Service', schema);

export default Service;
